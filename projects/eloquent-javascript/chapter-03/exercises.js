////////////////////////////////////////////////////////////////////////////////

function countChars(string, letter) {
  
  if (!string.length){
    return 0
  } else if(string.charAt(0) === letter){
    return 1 + countChars(string.slice(1), letter);
  } else {
    return countChars(string.slice(1), letter);
  }
  

}

