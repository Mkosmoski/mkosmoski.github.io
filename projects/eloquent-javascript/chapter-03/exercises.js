// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(wholeNum) {
  if(wholeNum === 0){
    return true;
  } else if (wholeNum === 1){
    return false;
  } else if (wholeNum > 0) {
    return isEven(wholeNum - 2);
  } else {
    return isEven(wholeNum +2);
  }

}

